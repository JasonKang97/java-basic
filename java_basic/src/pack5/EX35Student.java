package pack5;

public class EX35Student {	// record 형태의 class(집단 기억장소)로 자료 전달용(DTO)
	private String hakbun, irum;
	private int jumsu;
	
	
	
	public String getHakbun() {
		return hakbun;
	}
	public void setHakbun(String hakbun) {
		this.hakbun = hakbun;
	}
	public String getIrum() {
		return irum;
	}
	public void setIrum(String irum) {
		this.irum = irum;
	}
	public int getJumsu() {
		return jumsu;
	}
	public void setJumsu(int jumsu) {
		this.jumsu = jumsu;
	}
	
}
